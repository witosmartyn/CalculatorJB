public class Calculate {


    static double plus(double a, double b) {
         return a+b;
    }


    static double minus(double a, double b) {
        return a-b;
    }

    static double devide(double a, double b) {
        return a/b;
    }

    static double mult(double a, double b) {
        return a*b;
    } // calculate
}



